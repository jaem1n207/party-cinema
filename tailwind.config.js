const { withUt } = require('uploadthing/tw');

function pxToRem(px) {
  return `${px / 16}rem`;
}

function range(start, end, step = 1) {
  const length = (end - start) / step + 1;
  return Array.from({ length }, (_, i) => start + i * step);
}

